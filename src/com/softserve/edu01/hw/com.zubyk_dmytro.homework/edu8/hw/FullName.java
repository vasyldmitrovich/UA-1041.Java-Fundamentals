package edu8.hw;

class FullName {
    private String fisrtName;
    private String lastname;

    public FullName(String fisrtName, String lastName) {
        this.fisrtName = fisrtName;
        this.lastname = lastName;
    }

    public String getFisrtName() {
        return fisrtName;
    }

    public String getLastname() {
        return lastname;
    }
}
