package src.com.softserve.edu01.pt;

import java.util.Scanner;

public class ReverseString {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Write your first string: ");
        String srt1 = scanner.nextLine();
        System.out.print("Write your second string: ");
        String srt2 = scanner.nextLine();
        System.out.print("Write your third string: ");
        String srt3 = scanner.nextLine();

        System.out.println(srt3);
        System.out.println(srt2);
        System.out.println(srt1);
    }
}
